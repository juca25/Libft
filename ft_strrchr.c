char	*ft_strrchr(const char *s, int c)
{
	const char* last = NULL;
	int	i;
	
	i = 0;
	while(s[i] != 0)
	{
		if(s[i]  == c)
			last = &s[i];
		i++;
	}
	if(s[i] == c)
		last = &s[i];
	return (char *)last;
}